package com.clearmands.capacitacion.entidades;

public class Calculadora {
	//Suma
	public int suma(int a, int b) {
		return a+b;
	}
	
	//Resta
	public double resta(int a, int b) {
		return a-b;
	}
	
	//Multiplicar
	public double multiplicar(int a, int b) {
		return a*b;
	}
	
	
}
